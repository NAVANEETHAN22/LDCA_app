package com.example.ldca.results;

import com.itextpdf.kernel.pdf.*;
import com.itextpdf.layout.*;
import com.itextpdf.layout.element.*;
import com.itextpdf.io.image.*;

import android.content.Context;

public class PDFReportGenerator {

    public static void generate(Context ctx,
                                String path,
                                String datasetInfo,
                                String modelName,
                                ResultBundle r,
                                byte[] metricChartImg,
                                byte[] classChartImg,
                                byte[] rocImg,
                                byte[] prImg,
                                byte[] featureImg,
                                byte[] cvImg) {
        try {
            PdfWriter writer = new PdfWriter(path);
            PdfDocument pdf  = new PdfDocument(writer);
            Document doc     = new Document(pdf);

            // ── Header ──────────────────────────────────────────────────
            doc.add(new Paragraph("LDCA Analysis Report")
                    .setBold().setFontSize(20));
            doc.add(new Paragraph("Generated by LDCA Android App"));
            doc.add(new Paragraph("-----------------------------------"));

            // ── 1. Dataset Information ───────────────────────────────────
            doc.add(new Paragraph("1. Dataset Information").setBold());
            doc.add(new Paragraph(datasetInfo != null ? datasetInfo : "N/A"));
            doc.add(new Paragraph("\n"));

            // ── 2. Model Selection ───────────────────────────────────────
            doc.add(new Paragraph("2. Model Selection").setBold());
            doc.add(new Paragraph("Model Used: " + modelName));
            doc.add(new Paragraph("\n"));

            // ── 3. Evaluation Metrics ────────────────────────────────────
            doc.add(new Paragraph("3. Evaluation Metrics").setBold());
            doc.add(new Paragraph(String.format("Accuracy:  %.4f", r.accuracy)));
            doc.add(new Paragraph(String.format("Precision: %.4f", r.precision)));
            doc.add(new Paragraph(String.format("Recall:    %.4f", r.recall)));
            doc.add(new Paragraph(String.format("F1 Score:  %.4f", r.f1)));
            doc.add(new Paragraph("\n"));

            // ── 4. Metric Visualization ──────────────────────────────────
            if (metricChartImg != null) {
                doc.add(new Paragraph("4. Metric Visualization").setBold());
                Image img = new Image(ImageDataFactory.create(metricChartImg));
                img.setAutoScale(true);
                doc.add(img);
                doc.add(new Paragraph("\n"));
            }

            // ── 5. Class Distribution ────────────────────────────────────
            if (classChartImg != null) {
                doc.add(new Paragraph("5. Class Distribution").setBold());
                Image img = new Image(ImageDataFactory.create(classChartImg));
                img.setAutoScale(true);
                doc.add(img);
                doc.add(new Paragraph("\n"));
            }

            // ── 6. Confusion Matrix ──────────────────────────────────────
            doc.add(new Paragraph("6. Confusion Matrix").setBold());
            if (r.confusion != null && r.confusion.length >= 2) {
                Table table = new Table(2);
                table.addCell(new Cell().add(new Paragraph("TP: " + r.confusion[0][0])));
                table.addCell(new Cell().add(new Paragraph("FP: " + r.confusion[0][1])));
                table.addCell(new Cell().add(new Paragraph("FN: " + r.confusion[1][0])));
                table.addCell(new Cell().add(new Paragraph("TN: " + r.confusion[1][1])));
                doc.add(table);
            }
            doc.add(new Paragraph("\n"));

            // ── 7. Cross-Validation Results ──────────────────────────────
            doc.add(new Paragraph("7. Cross-Validation Results (5-Fold)").setBold());
            if (r.cvFoldAccuracies != null) {
                doc.add(new Paragraph(String.format("Mean CV Accuracy: %.4f", r.cvMeanAccuracy)));
                Table cvTable = new Table(r.cvFoldAccuracies.length);
                for (int i = 0; i < r.cvFoldAccuracies.length; i++) {
                    cvTable.addCell(new Cell().add(new Paragraph("Fold " + (i + 1))));
                }
                for (double acc : r.cvFoldAccuracies) {
                    cvTable.addCell(new Cell().add(new Paragraph(String.format("%.4f", acc))));
                }
                doc.add(cvTable);
                if (cvImg != null) {
                    Image img = new Image(ImageDataFactory.create(cvImg));
                    img.setAutoScale(true);
                    doc.add(img);
                }
            } else {
                doc.add(new Paragraph("(Cross-validation data not available)"));
            }
            doc.add(new Paragraph("\n"));

            // ── 8. ROC Curve ─────────────────────────────────────────────
            doc.add(new Paragraph("8. ROC Curve").setBold());
            if (r.aucScore > 0) {
                doc.add(new Paragraph(String.format("AUC Score: %.4f", r.aucScore)));
            }
            if (rocImg != null) {
                Image img = new Image(ImageDataFactory.create(rocImg));
                img.setAutoScale(true);
                doc.add(img);
            } else {
                doc.add(new Paragraph("(ROC curve not available — requires binary labels)"));
            }
            doc.add(new Paragraph("\n"));

            // ── 9. Precision-Recall Curve ─────────────────────────────────
            doc.add(new Paragraph("9. Precision-Recall Curve").setBold());
            if (prImg != null) {
                Image img = new Image(ImageDataFactory.create(prImg));
                img.setAutoScale(true);
                doc.add(img);
            } else {
                doc.add(new Paragraph("(PR curve not available — requires binary labels)"));
            }
            doc.add(new Paragraph("\n"));

            // ── 10. Feature Importance ────────────────────────────────────
            doc.add(new Paragraph("10. Feature Importance").setBold());
            if (r.featureImportances != null) {
                // Text table of top features
                int count = Math.min(r.featureImportances.length, 15);
                Table fiTable = new Table(2);
                fiTable.addCell(new Cell().add(new Paragraph("Feature").setBold()));
                fiTable.addCell(new Cell().add(new Paragraph("Importance").setBold()));
                for (int i = 0; i < count; i++) {
                    String fname = (r.featureNames != null && i < r.featureNames.length)
                            ? r.featureNames[i] : "Feature " + i;
                    fiTable.addCell(new Cell().add(new Paragraph(fname)));
                    fiTable.addCell(new Cell().add(new Paragraph(String.format("%.4f", r.featureImportances[i]))));
                }
                doc.add(fiTable);
                if (featureImg != null) {
                    Image img = new Image(ImageDataFactory.create(featureImg));
                    img.setAutoScale(true);
                    doc.add(img);
                }
            } else {
                doc.add(new Paragraph("(Feature importance not available for this model)"));
            }

            doc.close();

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
}
